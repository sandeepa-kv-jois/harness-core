package io.harness.delegate.beans.connector.azureconnector;

import io.harness.annotations.dev.HarnessTeam;
import io.harness.annotations.dev.OwnedBy;

import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonValue;

@OwnedBy(HarnessTeam.CDP)
public enum AzureSecretType {
  @JsonProperty(AzureConstants.SECRET_KEY) SECRET_KEY(AzureConstants.SECRET_KEY),
  @JsonProperty(AzureConstants.KEY_CERT) KEY_CERT(AzureConstants.KEY_CERT);

  private final String displayName;

  AzureSecretType(String displayName) {
    this.displayName = displayName;
  }

  public String getDisplayName() {
    return displayName;
  }

  @Override
  public String toString() {
    return displayName;
  }

  @JsonValue
  final String displayName() {
    return this.displayName;
  }

  public static AzureSecretType fromString(String typeEnum) {
    for (AzureSecretType enumValue : AzureSecretType.values()) {
      if (enumValue.getDisplayName().equals(typeEnum)) {
        return enumValue;
      }
    }
    throw new IllegalArgumentException("Invalid value: " + typeEnum);
  }
}
