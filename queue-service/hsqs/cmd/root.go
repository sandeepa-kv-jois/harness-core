package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "hsqs",
	Short: "Queue tasks library for harness",
	Long: `Use this app for enqueuing and de-queuing tasks from the queue. You can also start this as independent web service:

Examples:
hsqs server --port=<port>
hsqs queue --topic=<topic> --ns=<ns>
`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

